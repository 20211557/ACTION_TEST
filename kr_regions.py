def list_sido():
    """앱 UI 드롭다운용: 전국 17개 광역단체 풀네임 리스트"""
    return list(KR_SIDO.keys())


def get_sido_info(sido: str) -> dict:
    """광역단체 정보(region 코드 + 대표 좌표) 반환"""
    if sido not in KR_SIDO:
        raise ValueError(
            f"알 수 없는 광역단체: {sido}\n가능한 값: {list_sido()}"
        )
    return KR_SIDO[sido]


def is_learned(sido: str) -> bool:
    """그 광역단체가 학습 데이터에 포함되어 있는지"""
    return get_sido_info(sido)["region"] is not None
